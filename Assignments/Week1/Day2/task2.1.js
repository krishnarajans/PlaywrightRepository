let num = -4
function numType(num)
{
    if (num > 0)
        {
            console.log(`${num} is a positive number`)
        }
    else if (num < 0)
        {
            console.log(`${num} is a negative number`)
        }
    else
        {
            console.log(`${num} is a neutral number`)
        }
}

numType(num)
